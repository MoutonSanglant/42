#include "fdf.h"

void	init_grid(t_vector3 *grid)
{
	int		x;
	int		y;
	x = 0;
	y = 0;
	while (x < 10)
	{
		while(y < 10)
		{
			grid[x + y * 10].x = (double)(50 * x);
			grid[x + y * 10].y = (double)(50 * y);
			grid[x + y].z = 0.0;
			y++;
		}
		y = 0;
		x++;
	}
}
