/*
** Transpose a matrix to switch between column-vector
** matrices and row-vector matrices
*/
void		transpose_matrix4(t_matrix4 *m)
{
	t_matrix4	A;

	A[0] = (*m)[0];
	A[1] = (*m)[1];
	A[2] = (*m)[2];
	A[3] = (*m)[3];
	A[4] = (*m)[4];
	A[5] = (*m)[5];
	A[6] = (*m)[6];
	A[7] = (*m)[7];
	A[8] = (*m)[8];
	A[9] = (*m)[9];
	A[10] = (*m)[10];
	A[11] = (*m)[11];
	A[12] = (*m)[12];
	A[13] = (*m)[13];
	A[14] = (*m)[14];
	A[15] = (*m)[15];

	(*m)[0] = A[0];
	(*m)[1] = A[4];
	(*m)[2] = A[8];
	(*m)[3] = A[12];
	(*m)[4] = A[1];
	(*m)[5] = A[5];
	(*m)[6] = A[9];
	(*m)[7] = A[13];
	(*m)[8] = A[2];
	(*m)[9] = A[6];
	(*m)[10] = A[10];
	(*m)[11] = A[14];
	(*m)[12] = A[3];
	(*m)[13] = A[7];
	(*m)[14] = A[11];
	(*m)[15] = A[15];
}

